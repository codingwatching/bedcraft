template <typename BlockType, typename... Args>
BlockType* registerBlock(const std::string& name, Args&&... args) {
    const auto id = static_cast<int>(Blocks::pallete.getId(name));
    if (Blocks::blocks.size() < id + 1) {
        Blocks::blocks.resize(id + 1);
    }

    auto block = new BlockType(id, std::forward<Args>(args)...);
    block->graphics = BlockGraphics::mBlockLookupMap.at(name);

    Blocks::blocks[(int) id] = block;

    return block;
}

