template <typename BlockType, typename... Args>
BlockType& registerBlock(BlockTable &pallete, const std::string& name, Args&&... args) {
    const auto id = static_cast<int>(pallete.getId(name));
    if (Blocks::id_to_block.size() < id + 1) {
        Blocks::id_to_block.resize(id + 1);
    }

    auto block = new BlockType(id);
    block->graphics = BlockGraphics::mBlockLookupMap.at(name);

    Blocks::id_to_block[(int) id] = block;

    return *block;
}

